package com.store.jewelry.domain;

public class ChainsSize {
	
	public Integer chainsSizeId;
	public Float chainsSize;
	public Integer quantityChainsSize;
	public boolean chainsSizeFlag;
	public ChainsSize() {
		super();
	}
	public Integer getChainsSizeId() {
		return chainsSizeId;
	}
	public void setChainsSizeId(Integer chainsSizeId) {
		this.chainsSizeId = chainsSizeId;
	}
	public Float getChainsSize() {
		return chainsSize;
	}
	public void setChainsSize(Float chainsSize) {
		this.chainsSize = chainsSize;
	}
	public Integer getQuantityChainsSize() {
		return quantityChainsSize;
	}
	public void setQuantityChainsSize(Integer quantityChainsSize) {
		this.quantityChainsSize = quantityChainsSize;
	}
	public boolean isChainsSizeFlag() {
		return chainsSizeFlag;
	}
	public void setChainsSizeFlag(boolean chainsSizeFlag) {
		this.chainsSizeFlag = chainsSizeFlag;
	}
	

}
